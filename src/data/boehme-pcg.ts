// Recherche par mots-clés
export function findBoehmeAccount(description: string, supplierName: string): BoehmeAccount | null {
  const searchText = `${description} ${supplierName}`.toLowerCase()
  let bestMatch: BoehmeAccount | null = null
  let bestScore = 0

  for (const account of BOEHME_PCG) {
    let score = 0
    for (const keyword of account.keywords) {
      if (searchText.includes(keyword.toLowerCase())) {
        score += keyword.length // Longer keyword matches score higher
      }
    }
    if (score > bestScore) {
      bestScore = score
      bestMatch = account
    }
  }

  return bestScore > 0 ? bestMatch : null
}
